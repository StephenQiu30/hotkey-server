CREATE OR REPLACE FUNCTION enforce_monitor_config_pointers()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    draft_state varchar(16);
    published_state varchar(16);
BEGIN
    IF NEW.draft_config_version_id IS NOT NULL THEN
        SELECT state INTO draft_state FROM monitor_config_versions WHERE id = NEW.draft_config_version_id AND monitor_id = NEW.id;
        IF draft_state IS DISTINCT FROM 'draft' THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitors_draft_config_version_owner_check', MESSAGE = 'draft configuration does not belong to monitor or is not a draft';
        END IF;
    END IF;
    IF NEW.published_config_version_id IS NOT NULL THEN
        SELECT state INTO published_state FROM monitor_config_versions WHERE id = NEW.published_config_version_id AND monitor_id = NEW.id;
        IF published_state IS DISTINCT FROM 'published' THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitors_published_config_version_owner_check', MESSAGE = 'published configuration does not belong to monitor or is not published';
        END IF;
    END IF;
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS monitors_config_pointer_owner ON monitors;
CREATE TRIGGER monitors_config_pointer_owner
BEFORE INSERT OR UPDATE OF draft_config_version_id, published_config_version_id ON monitors
FOR EACH ROW EXECUTE FUNCTION enforce_monitor_config_pointers();

CREATE OR REPLACE FUNCTION validate_source_connection_configuration()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    config_key text;
BEGIN
    IF jsonb_typeof(NEW.config) <> 'object' THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'source_connections_config_object_check', MESSAGE = 'source config must be an object';
    END IF;
    FOR config_key IN SELECT jsonb_object_keys(NEW.config)
    LOOP
        IF config_key NOT IN ('allow_body_storage', 'requires_attribution', 'requires_deletion_sync', 'content_retention_days', 'metrics_retention_days', 'allowed_languages', 'allowed_regions', 'rate_limit_per_minute', 'request_timeout_seconds', 'max_pages_per_run') THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'source_connections_config_whitelist_check', MESSAGE = 'source config contains an unsupported key';
        END IF;
    END LOOP;
    IF (NEW.config ? 'allow_body_storage' AND jsonb_typeof(NEW.config->'allow_body_storage') <> 'boolean')
       OR (NEW.config ? 'requires_attribution' AND jsonb_typeof(NEW.config->'requires_attribution') <> 'boolean')
       OR (NEW.config ? 'requires_deletion_sync' AND jsonb_typeof(NEW.config->'requires_deletion_sync') <> 'boolean') THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'source_connections_config_boolean_check', MESSAGE = 'source config boolean value is invalid';
    END IF;
    IF (NEW.config ? 'content_retention_days' AND (jsonb_typeof(NEW.config->'content_retention_days') <> 'number' OR (NEW.config->>'content_retention_days')::numeric <> trunc((NEW.config->>'content_retention_days')::numeric) OR (NEW.config->>'content_retention_days')::integer NOT BETWEEN 1 AND 3650))
       OR (NEW.config ? 'metrics_retention_days' AND (jsonb_typeof(NEW.config->'metrics_retention_days') <> 'number' OR (NEW.config->>'metrics_retention_days')::numeric <> trunc((NEW.config->>'metrics_retention_days')::numeric) OR (NEW.config->>'metrics_retention_days')::integer NOT BETWEEN 1 AND 3650))
       OR (NEW.config ? 'rate_limit_per_minute' AND (jsonb_typeof(NEW.config->'rate_limit_per_minute') <> 'number' OR (NEW.config->>'rate_limit_per_minute')::numeric <> trunc((NEW.config->>'rate_limit_per_minute')::numeric) OR (NEW.config->>'rate_limit_per_minute')::integer NOT BETWEEN 1 AND 600))
       OR (NEW.config ? 'request_timeout_seconds' AND (jsonb_typeof(NEW.config->'request_timeout_seconds') <> 'number' OR (NEW.config->>'request_timeout_seconds')::numeric <> trunc((NEW.config->>'request_timeout_seconds')::numeric) OR (NEW.config->>'request_timeout_seconds')::integer NOT BETWEEN 1 AND 120))
       OR (NEW.config ? 'max_pages_per_run' AND (jsonb_typeof(NEW.config->'max_pages_per_run') <> 'number' OR (NEW.config->>'max_pages_per_run')::numeric <> trunc((NEW.config->>'max_pages_per_run')::numeric) OR (NEW.config->>'max_pages_per_run')::integer NOT BETWEEN 1 AND 20)) THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'source_connections_config_integer_check', MESSAGE = 'source config integer value is invalid';
    END IF;
    IF (NEW.config ? 'allowed_languages' AND (jsonb_typeof(NEW.config->'allowed_languages') <> 'array' OR jsonb_array_length(NEW.config->'allowed_languages') > 8 OR EXISTS (SELECT 1 FROM jsonb_array_elements(NEW.config->'allowed_languages') AS value WHERE jsonb_typeof(value) <> 'string')))
       OR (NEW.config ? 'allowed_regions' AND (jsonb_typeof(NEW.config->'allowed_regions') <> 'array' OR jsonb_array_length(NEW.config->'allowed_regions') > 8 OR EXISTS (SELECT 1 FROM jsonb_array_elements(NEW.config->'allowed_regions') AS value WHERE jsonb_typeof(value) <> 'string'))) THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'source_connections_config_array_check', MESSAGE = 'source config array value is invalid';
    END IF;
    IF NEW.source_type = 'hacker_news' AND NEW.endpoint <> 'https://hacker-news.firebaseio.com/v0' THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'source_connections_hacker_news_endpoint_check', MESSAGE = 'hacker news endpoint must be the official endpoint';
    END IF;
    IF TG_OP = 'UPDATE'
       AND (NEW.source_type IS DISTINCT FROM OLD.source_type OR NEW.endpoint IS DISTINCT FROM OLD.endpoint OR NEW.config IS DISTINCT FROM OLD.config)
       AND EXISTS (
           SELECT 1
           FROM monitor_sources AS monitor_source
           JOIN monitor_config_versions AS config_version ON config_version.id = monitor_source.config_version_id
           WHERE monitor_source.source_connection_id = OLD.id AND config_version.state IN ('published', 'superseded')
       ) THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'source_connections_semantic_immutable', MESSAGE = 'published monitor configuration source semantics are immutable';
    END IF;
    RETURN NEW;
END;
$$;

