    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, task_type varchar(64) NOT NULL, target_type varchar(64) NOT NULL, target_id bigint NOT NULL,
    model_profile_id bigint NOT NULL REFERENCES ai_model_profiles(id) ON DELETE RESTRICT, prompt_version varchar(64) NOT NULL, schema_version varchar(64) NOT NULL,
    input_hash char(64) NOT NULL, request_object_key text, response_object_key text, structured_result jsonb NOT NULL DEFAULT '{}'::jsonb,
    tokens bigint NOT NULL DEFAULT 0 CHECK (tokens >= 0), cost numeric(12,4) NOT NULL DEFAULT 0 CHECK (cost >= 0), latency_ms bigint NOT NULL DEFAULT 0 CHECK (latency_ms >= 0),
    status varchar(16) NOT NULL CHECK (status IN ('queued','running','validating','retry_wait','succeeded','failed','cancelled')),
    created_at timestamptz NOT NULL DEFAULT now(), started_at timestamptz, finished_at timestamptz,
    model_profile_version bigint NOT NULL,
    model_version varchar(64) NOT NULL,
    parameters_version varchar(64) NOT NULL,
    input_schema_version varchar(64) NOT NULL,
    evidence_set_hash char(64) NOT NULL,
    reuse_key char(64) NOT NULL,
    attempt smallint NOT NULL DEFAULT 1 CHECK (attempt BETWEEN 1 AND 3),
    max_attempts smallint NOT NULL DEFAULT 1 CHECK (max_attempts BETWEEN 1 AND 3),
    repair_attempted boolean NOT NULL DEFAULT false,
    retry_after timestamptz,
    error_code integer,
    budget_day date NOT NULL,
    reserved_cost numeric(12,4) NOT NULL DEFAULT 0 CHECK (reserved_cost >= 0),
    lease_expires_at timestamptz,
    CHECK (request_object_key IS NULL AND response_object_key IS NULL),
    CHECK (attempt <= max_attempts),
    CHECK (status IN ('queued','running','validating','retry_wait') AND lease_expires_at IS NOT NULL OR status IN ('succeeded','failed','cancelled') AND lease_expires_at IS NULL)
);
CREATE UNIQUE INDEX IF NOT EXISTS ai_runs_reuse_succeeded_uq ON ai_runs(reuse_key) WHERE status = 'succeeded';
CREATE UNIQUE INDEX IF NOT EXISTS ai_runs_reuse_inflight_uq ON ai_runs(reuse_key) WHERE status IN ('queued','running','validating','retry_wait');

