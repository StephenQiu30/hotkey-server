CREATE TABLE IF NOT EXISTS source_connections (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version bigint NOT NULL DEFAULT 1,
    source_type varchar(24) NOT NULL CHECK (source_type IN ('rss','hacker_news')),
    name varchar(120) NOT NULL, endpoint text NOT NULL,
    auth_type varchar(24) NOT NULL DEFAULT 'none' CHECK (auth_type IN ('none','api_key','oauth2','bearer')),
    credential_ref text CHECK ((auth_type = 'none' AND credential_ref IS NULL) OR (auth_type <> 'none' AND credential_ref ~ '^env:[A-Z_][A-Z0-9_]{0,127}$')),
