-- River job metadata belongs to the job-queue infrastructure and is not exposed
-- through application repositories or public APIs. The runtime integration in
-- PLAN-002 validates its expected River version before workers are enabled.
CREATE TABLE IF NOT EXISTS river_job (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, kind text NOT NULL, args jsonb NOT NULL DEFAULT '{}'::jsonb,
    metadata jsonb NOT NULL DEFAULT '{}'::jsonb, state text NOT NULL DEFAULT 'available', attempt integer NOT NULL DEFAULT 0,
    max_attempts integer NOT NULL DEFAULT 25, priority smallint NOT NULL DEFAULT 1, scheduled_at timestamptz NOT NULL DEFAULT now(),
    attempted_at timestamptz, finalized_at timestamptz, unique_key bytea, tags varchar(255)[] NOT NULL DEFAULT '{}',
    errors jsonb[] NOT NULL DEFAULT '{}', created_at timestamptz NOT NULL DEFAULT now(), UNIQUE NULLS NOT DISTINCT (kind, unique_key)
);
CREATE INDEX IF NOT EXISTS river_job_fetch_idx ON river_job(state, scheduled_at, priority, id);

CREATE TABLE IF NOT EXISTS river_job_attempt (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, job_id bigint NOT NULL REFERENCES river_job(id) ON DELETE CASCADE,
    attempt integer NOT NULL, error text, created_at timestamptz NOT NULL DEFAULT now(), UNIQUE (job_id, attempt)
);

CREATE TABLE IF NOT EXISTS river_queue (
    name text PRIMARY KEY, paused_at timestamptz, created_at timestamptz NOT NULL DEFAULT now(), updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS river_leader (
    name text PRIMARY KEY, elected_at timestamptz NOT NULL DEFAULT now(), expires_at timestamptz NOT NULL
);

CREATE TABLE IF NOT EXISTS river_migration (
    version bigint PRIMARY KEY, created_at timestamptz NOT NULL DEFAULT now()
