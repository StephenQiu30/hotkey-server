CREATE OR REPLACE FUNCTION enforce_monitor_config_version_immutability()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF TG_OP = 'INSERT' THEN
        IF NEW.state <> 'draft' OR NEW.config_hash IS NOT NULL OR NEW.published_at IS NOT NULL THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitor_config_versions_initial_draft_check', MESSAGE = 'monitor configuration versions must be created as drafts';
        END IF;
        RETURN NEW;
    END IF;

    IF TG_OP = 'DELETE' THEN
        IF EXISTS (
            SELECT 1
            FROM monitors
            WHERE draft_config_version_id = OLD.id OR published_config_version_id = OLD.id
        ) THEN
            RETURN OLD;
        END IF;
        IF OLD.state <> 'draft' THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitor_config_versions_immutable', MESSAGE = 'published monitor configuration versions are immutable';
        END IF;
        RETURN OLD;
    END IF;

    IF OLD.state = 'superseded' THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitor_config_versions_immutable', MESSAGE = 'superseded monitor configuration versions are immutable';
    END IF;
    IF OLD.state = 'published' THEN
        IF NEW.state <> 'superseded'
           OR NEW.monitor_id IS DISTINCT FROM OLD.monitor_id
           OR NEW.revision IS DISTINCT FROM OLD.revision
           OR NEW.version IS DISTINCT FROM OLD.version
           OR NEW.timezone IS DISTINCT FROM OLD.timezone
           OR NEW.languages IS DISTINCT FROM OLD.languages
           OR NEW.regions IS DISTINCT FROM OLD.regions
           OR NEW.collection_interval_seconds IS DISTINCT FROM OLD.collection_interval_seconds
           OR NEW.relevance_threshold IS DISTINCT FROM OLD.relevance_threshold
           OR NEW.event_threshold IS DISTINCT FROM OLD.event_threshold
           OR NEW.retention_days IS DISTINCT FROM OLD.retention_days
           OR NEW.config_hash IS DISTINCT FROM OLD.config_hash
           OR NEW.published_at IS DISTINCT FROM OLD.published_at
           OR NEW.created_by IS DISTINCT FROM OLD.created_by
           OR NEW.updated_by IS DISTINCT FROM OLD.updated_by
           OR NEW.created_at IS DISTINCT FROM OLD.created_at
           OR NEW.updated_at IS DISTINCT FROM OLD.updated_at THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitor_config_versions_immutable', MESSAGE = 'published monitor configuration versions are immutable';
        END IF;
        RETURN NEW;
    END IF;

    IF NEW.state = 'published' THEN
        IF NEW.config_hash IS NULL OR NEW.published_at IS NULL THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitor_config_versions_publish_check', MESSAGE = 'published monitor configuration requires hash and timestamp';
        END IF;
    ELSIF NEW.state <> 'draft' THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = 'monitor_config_versions_state_transition_check', MESSAGE = 'draft configuration must publish before superseding';
    END IF;
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS monitor_config_versions_immutable ON monitor_config_versions;
CREATE TRIGGER monitor_config_versions_immutable
BEFORE INSERT OR UPDATE OR DELETE ON monitor_config_versions
FOR EACH ROW EXECUTE FUNCTION enforce_monitor_config_version_immutability();

CREATE OR REPLACE FUNCTION enforce_monitor_config_child_immutability()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    current_config_version_id bigint;
    current_state varchar(16);
    prior_state varchar(16);
BEGIN
    IF TG_OP = 'DELETE' THEN
        current_config_version_id := OLD.config_version_id;
    ELSE
        current_config_version_id := NEW.config_version_id;
    END IF;
    SELECT state INTO current_state FROM monitor_config_versions WHERE id = current_config_version_id;
    IF current_state IS DISTINCT FROM 'draft' THEN
        RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = TG_TABLE_NAME || '_immutable', MESSAGE = 'published monitor configuration children are immutable';
    END IF;
    IF TG_OP = 'UPDATE' AND NEW.config_version_id IS DISTINCT FROM OLD.config_version_id THEN
        SELECT state INTO prior_state FROM monitor_config_versions WHERE id = OLD.config_version_id;
        IF prior_state IS DISTINCT FROM 'draft' THEN
            RAISE EXCEPTION USING ERRCODE = '23514', CONSTRAINT = TG_TABLE_NAME || '_immutable', MESSAGE = 'published monitor configuration children are immutable';
        END IF;
    END IF;
    RETURN CASE WHEN TG_OP = 'DELETE' THEN OLD ELSE NEW END;
END;
$$;

