CREATE OR REPLACE FUNCTION enforce_auth_refresh_token_expiry()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    session_absolute_expires_at timestamptz;
BEGIN
    SELECT absolute_expires_at INTO session_absolute_expires_at
    FROM auth_sessions
    WHERE id = NEW.session_id;
    IF NOT FOUND THEN
        RETURN NEW;
    END IF;
    IF NEW.expires_at > session_absolute_expires_at THEN
        RAISE EXCEPTION USING
            ERRCODE = '23514',
            CONSTRAINT = 'auth_refresh_tokens_expires_within_session_check',
            MESSAGE = 'auth refresh token expiry must not exceed session absolute expiry';
    END IF;
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS auth_refresh_tokens_expires_within_session_check ON auth_refresh_tokens;
CREATE TRIGGER auth_refresh_tokens_expires_within_session_check
BEFORE INSERT OR UPDATE OF session_id, expires_at ON auth_refresh_tokens
FOR EACH ROW EXECUTE FUNCTION enforce_auth_refresh_token_expiry();

