-- River job metadata belongs to the job-queue infrastructure and is not exposed
-- through application repositories or public APIs. The runtime integration in
-- PLAN-002 validates its expected River version before workers are enabled.
CREATE TABLE IF NOT EXISTS river_job (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, kind text NOT NULL, args jsonb NOT NULL DEFAULT '{}'::jsonb,
    metadata jsonb NOT NULL DEFAULT '{}'::jsonb, state text NOT NULL DEFAULT 'available', attempt integer NOT NULL DEFAULT 0,
    max_attempts integer NOT NULL DEFAULT 25, priority smallint NOT NULL DEFAULT 1, scheduled_at timestamptz NOT NULL DEFAULT now(),
