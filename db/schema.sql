CREATE OR REPLACE FUNCTION enforce_auth_session_absolute_expiry_immutable()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.absolute_expires_at IS DISTINCT FROM OLD.absolute_expires_at THEN
        RAISE EXCEPTION USING
            ERRCODE = '23514',
            CONSTRAINT = 'auth_sessions_absolute_expiry_immutable',
            MESSAGE = 'auth session absolute expiry is immutable';
    END IF;
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS auth_sessions_absolute_expiry_immutable ON auth_sessions;
CREATE TRIGGER auth_sessions_absolute_expiry_immutable
BEFORE UPDATE OF absolute_expires_at ON auth_sessions
FOR EACH ROW EXECUTE FUNCTION enforce_auth_session_absolute_expiry_immutable();

